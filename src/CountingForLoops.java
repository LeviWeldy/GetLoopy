public static void main(String[] args) {


    //Counting For Loops #1
    System.out.println("Problem #1: Counting For Loops");
    for (int i = 0; i <= 30; i++) {
        System.out.print(i + " ");
    }

    System.out.println(""); // print a new line for better formatting
    System.out.println("");


    //Counting For Loops #2
    System.out.println("Problem #2: Counting For Loops");
    for (int i = 30; i >= 0; i--) {
        System.out.print(i + " ");
    }

    System.out.println(""); // print a new line for better formatting
    System.out.println("");

//Counting For Loops #3
    System.out.println("Problem #3: Counting For Loops");
    for (int i = 0; i <= 18; i++, i++, i++) {
        System.out.print(i + " ");
    }

    System.out.println(""); // print a new line for better formatting
    System.out.println("");


    //Counting For Loops #4
    System.out.println("Problem #4: Counting For Loops");
    for (int i = 10; i >= 0; i--, i--) {
        System.out.print(i + " ");
    }
}